# Exam Oracle 3000 - Forward Propagation Skeleton (1 Hidden Layer)

import numpy as np

# 1. Define sigmoid activation (In case you have tried different activation function code that here instead of sigmoid)
def sigmoid(z):
    return 1/(1+np.exp(-z))

# 2. Input (Student Profile)
X = np.array([8.0,0.90,0.85])   # Fill with your chosen 3 parameters [e.g. - study_hours, attendance, quiz_score]

# 3. Initialize weights and biases (Set your own weights and biases!)
# Hidden layer
W1 = np.array([[0.5, 0.2, 0.1],
               [0.3, 0.4, 0.3],
               [0.2, 0.1, 0.6]])
b1 = np.array([0.0,0.0,0.0])

# Output layer (2 neurons: Pass, Fail)
W2 = np.array([[0.6, 0.5, 0.4],
               [0.4, 0.5, 0.6]])
b2 = np.array([0.0,0.0])

# 4. Forward Propagation
# Input ‚Üí Hidden
Z1 =   np.dot(W1, X) + b1
A1 =   sigmoid(Z1)

# Hidden ‚Üí Output  (Try thinking this on your own)
Z2 =  np.dot(W2, A1) + b2
A2 = sigmoid(Z2)

# 5. Final Prediction
print("Oracle prediction (Pass, Fail):", A2)


